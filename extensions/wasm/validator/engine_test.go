func TestPolicyEngine_Run_success(t *testing.T) {
	t.Parallel()
	ctx := context.Background()
	eng, err := validator.NewPolicyEngine(ctx, minimalPolicyWasm)
	if err != nil {
		t.Fatal(err)
	}
	defer func() { _ = eng.Close(ctx) }()

	if err := eng.Run(ctx, []byte(`{"key":"value"}`)); err != nil {
		t.Errorf("Run with valid JSON: %v", err)
	}
}

func TestPolicyEngine_Run_reject(t *testing.T) {
	t.Parallel()
	ctx := context.Background()
	eng, err := validator.NewPolicyEngine(ctx, minimalRejectPolicyWasm)
	if err != nil {
		t.Fatal(err)
	}
	defer func() { _ = eng.Close(ctx) }()

	err = eng.Run(ctx, []byte(`{}`))
	if err == nil {
		t.Fatal("expected error from rejecting policy")
	}
	if !strings.Contains(err.Error(), "validation failed") {
		t.Errorf("error should mention validation failed: %v", err)
	}
}

func TestPolicyEngine_Close(t *testing.T) {
	t.Parallel()
	ctx := context.Background()
	eng, err := validator.NewPolicyEngine(ctx, minimalPolicyWasm)
	if err != nil {
		t.Fatal(err)
	}
	if err := eng.Close(ctx); err != nil {
		t.Errorf("Close: %v", err)
	}
	// Run after Close should fail (runtime closed)
	if err := eng.Run(ctx, []byte(`{}`)); err == nil {
		t.Error("Run after Close should return error")
	}
}

func TestPolicyEngine_Run_missingExports(t *testing.T) {
	t.Parallel()
	ctx := context.Background()
	// Minimal valid WASM with no exports (type 0, func 0, memory 0, code 0)
	empty := []byte{
		0x00, 0x61, 0x73, 0x6d, 0x01, 0x00, 0x00, 0x00,
		0x01, 0x04, 0x01, 0x60, 0x00, 0x00, // type
		0x03, 0x02, 0x01, 0x00, // function
		0x05, 0x03, 0x01, 0x00, 0x01, // memory
		0x0a, 0x04, 0x01, 0x02, 0x00, 0x0b, // code: 1 body (empty)
	}
	eng, err := validator.NewPolicyEngine(ctx, empty)
	if err != nil {
		t.Fatal(err)
	}
	defer func() { _ = eng.Close(ctx) }()
	if err := eng.Run(ctx, []byte("{}")); err == nil {
		t.Fatal("expected error when module has no validation exports")
	}
}
